package arraysdemo;

public class ForEachLoopSingleDimensionalArraysDemo {

	public static void main(String[] args) {
		
		int[] ar = {9,5,7,1,8,4,3,2,6,0,11,99,22,77,45,54,63,36,89,78};
		
		for(int e : ar) {
			System.out.println(e); // 9 5 7 1 8 4 3 2 6 0 11 99 22 77 45 54 63 36 89 78
		}

	}

}
