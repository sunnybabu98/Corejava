package variablespack;

public class DeclaringAndInitializingVariables {

	public static void main(String[] args) {
		
		int a = 5;  // Declaration and Initialization
		
//		int a = 10; // This is not possible - Duplicate variable
		
		int b; // Declaration statement
		b = 10; // Initialization statement
		
		int c = 15;
		int d = 20;
		
		int e = 25, f = 30;
		
		int g,h;
		
		g = 35;
		h = 40;
		
		int i = 45;
		double j = 4.32;
		
		int k = 12; // Initialization
		k = 13; // Re-initialization/Updation
		k = 14; // Re-initialization/Updation
		k = 15; // Re-initialization/Updation
		k = 16; // Re-initialization/Updation
		System.out.println(k);
		
		int a1,b1,c1,d1,e1;
		
		a1 = b1 = c1 = d1 = e1 = 5;
		
		System.out.println(a1);  // 5
		System.out.println(b1);  // 5
		System.out.println(c1);  // 5
		System.out.println(d1);  // 5
		System.out.println(e1);  // 5
		
//		System.out.println(m); // This is not possible
		int m = 65;
		System.out.println(m);
		int n;
//		System.out.println(n); // This is not possible 

	}

}
