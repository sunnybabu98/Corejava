package operatorspack;

public class RelationOperatorsDemoTwo {

	public static void main(String[] args) {

		int a = 5, b = 4, c = 5, d = 3, e = 7, f = 5;

		boolean i = a > b; // true
		boolean j = a < b; // false
		boolean k = a >= f; // true
		
		System.out.println(i); // true
		System.out.println(j); // false
		System.out.println(k); // true

	}

}
