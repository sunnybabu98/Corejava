package datatypespack;

public class DifferentPrimitiveDataTypesDemo {

	public static void main(String[] args) {
		
		int a = 999; // 1 byte = 8 bits
		double b = 5.123;
		char c = 's';
		boolean d = false;
		System.out.println(a); // 999
		System.out.println(b); // 5.123
		System.out.println(c); // s
		System.out.println(d); // false

	}

}
