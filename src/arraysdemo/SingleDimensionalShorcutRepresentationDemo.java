package arraysdemo;

public class SingleDimensionalShorcutRepresentationDemo {

	public static void main(String[] args) {
		
		int[] ar = {9,5,7,1,8,3,2,6,4};  // Shortcut representation of the array
		
		System.out.println(ar[0]);  // 9
		System.out.println(ar[1]);  // 5
		System.out.println(ar[2]); // 7
		System.out.println(ar[3]); // 1
		System.out.println(ar[4]); // 8
		System.out.println(ar[5]); // 3
		System.out.println(ar[6]); // 2
		System.out.println(ar[7]); // 6
		System.out.println(ar[8]); // 4

	}

}
