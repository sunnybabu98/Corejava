package operatorspack;

public class ConditionalOperatorDemo {

	public static void main(String[] args) {
		
		// ?:
		
		int a = 5, b = 14, c;
		
		c = a > b ? 9 : 19;
		
		System.out.println(c);  // 19
		
		int i = 7, j = 17;
		
		double k;
		
		k = i > j ? 1.2 : 2.3;
		
		System.out.println(k);  // 2.3

	}

}
