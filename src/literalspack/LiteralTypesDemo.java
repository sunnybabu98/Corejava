package literalspack;

public class LiteralTypesDemo {

	public static void main(String[] args) {
		
		int a = 9;  // Integer Literal
		byte b = 99; // Integer Literal
		short c = 999; // Integer Literal
		long d = 9999; // Integer Literal
		
		// byte e = 99999; // Integer Literal // -128 to 127
		byte e = 127; // Integer literal within the range of byte
		
		long f = 99L;  // Long literal
		
		long g = 2147483647; // Integer Literal
		
		//long h = 2147483648; // Integer literal out of range
		
		long h = 9223372036854775807L; // Long literal 
		
		//int k1 = 99L;  // Long literal
		
		double i = 1.23; // double literal
		double j = 1.23D; // double literal 
		
		double k = 1.23F; // Floating point literal
		
		//float l = 1.23;  // double literal
		float m = 1.23F; // floating point literal
		
		boolean b1 = true; // boolean literal
		boolean b2 = false; // boolean literal
		
		char c1 = 's'; // character literal
		int c2 = 's'; //  character literal
		char c3 = 111; // Integer literal
		int c4 = 112; // Integer literal
		
		String str = "Arun Motoori"; //String literal

	}

}
