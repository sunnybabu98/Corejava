package operatorspack;

public class LogicalOperatorsVariablesDemo {

	public static void main(String[] args) {

		boolean a = true, b = true, c = false, d = false;

		boolean e = a && b;
		boolean f = a || d;
		boolean g = !c;
		
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);

	}

}
