package stringhandling;

public class StringClassDemo {

	public static void main(String[] args) {
		
		byte a = 5;
		short b = 10;
		int c = 15;
		long d = 20;
		float e = 1.23f;
		double f = 1.112;
		char g = 's';
		boolean h = false;
		String i = "Arun Motoori"; //String is a pre-defined class and is not a primitive data type
		
		//Actual Represent of String
		String str1 = new String("Arun Motoori");
		//Shortcut Representation of String
		String str2 = "Arun Motoori";

	}

}
