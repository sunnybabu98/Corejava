package methodspack;

public class MultipleMethodsDemoTwo {

	public static void main(String[] args) {
		
		System.out.println("Start of main method");
		
		sampleMethodOne();  // Method calling statement
		sampleMethodTwo();  // Method calling statement
		sampleMethodThree(); // Method calling statement
		
		System.out.println("End of main method");

	}
	
	public static void sampleMethodOne() {
		
		System.out.println("Inside sampleMethodOne");
		
	}
	
	public static void sampleMethodTwo() {
		
		System.out.println("Inside sampleMethodTwo");
		
	}
	
	public static void sampleMethodThree() {
		
		System.out.println("Inside sampleMethodThree");
		
	}

}
